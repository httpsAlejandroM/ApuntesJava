public class Condicionales {
    public static void main(String[] args) throws Exception {

        int edad = 18;

        if (edad > 18) {
            System.out.println("Tienes la edad suficiente");
        } else if (edad == 18) {
            System.out.println("Tienes la edad justa");
        } else {
            System.out.println("No tienes edad suficiente");
        }
    }
}
